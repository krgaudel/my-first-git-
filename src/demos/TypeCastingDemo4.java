package demos;

public class TypeCastingDemo4 {
public static void main(String[] args) {
	char c1=97;
	short s1=97;
	System.out.println(c1);// converts 97 into character and prints a
	System.out.println(s1);//prints value  97
	
	
	char c2='a';
	short s2='a';
	System.out.println(c2);// prints char a
	System.out.println(s2);// converts char a to corresponding value

}

}
